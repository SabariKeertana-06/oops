package pack1;
public class MathOperations{
    int a,b;
    public MathOperations(int a, int b) {
        this.a = a;
        this.b = b;
    }
    public void add(int a,int b){
        System.out.println(a+b);
    }
    public void subtract(int a,int b){
        System.out.println( a-b);
    }
}
